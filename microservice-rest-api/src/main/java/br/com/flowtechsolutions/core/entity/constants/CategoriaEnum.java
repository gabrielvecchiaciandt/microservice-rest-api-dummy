package br.com.flowtechsolutions.core.entity.constants;

/**
 * Enum representando as categorias de produtos disponíveis no sistema.
 * Define os tipos de produtos que podem ser cadastrados no catálogo.
 */
public enum CategoriaEnum {
    /**
     * Produtos de informática (computadores, notebooks, periféricos, etc.)
     */
    INFORMATICA,
    
    /**
     * Produtos eletrônicos (TVs, smartphones, tablets, etc.)
     */
    ELETRONICOS,
    
    /**
     * Móveis (mesas, cadeiras, armários, etc.)
     */
    MOVEIS,
    
    /**
     * Livros e publicações
     */
    LIVROS,
    
    /**
     * Outros produtos que não se enquadram nas categorias principais
     */
    OUTROS
}